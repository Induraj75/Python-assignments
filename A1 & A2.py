# 1. In a short written piece, explain the difference between Interactive Mode and Scripting Mode in Python

# Interactive Mode is when you run Python commands one at a time directly in the Python shell or REPL (Read-Eval-Print Loop). Itâ€™s great for quick experiments, testing small snippets, or learning because you get immediate feedback after each command.

# Scripting Mode, on the other hand, involves writing multiple lines of Python code in a file (a script) and then running the entire file at once. This is ideal for larger programs or tasks that need to be repeated, as it allows you to save, edit, and run your code anytime.

# In short, Interactive Mode is for immediate, line-by-line execution, while Scripting Mode is for writing and running whole programs saved as files.

